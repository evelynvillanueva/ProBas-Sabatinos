 #Ejercicio 10: Calculadora de área de un triángulo. Descripción: Escribe un programa que calcule el área de un triángulo, dados su base y altura. 

b = float(input("Ingrese la base del triángulo: "))
al = float(input("Ingrese la altura del triángulo: "))

ar = (b * al) / 2

print("El área del triángulo es:", ar)

