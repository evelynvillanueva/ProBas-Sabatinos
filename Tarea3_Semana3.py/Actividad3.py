#Ejercicio 3: Función Lambda. Descripción: Escribe una función lambda que reciba dos números y devuelva el producto. 

"""
def producto(x,y): 
    return x * y 

resultado = producto(5,7)
print(f"El producto es: {resultado}") 
"""

producto = lambda x, y: x * y

resultado = producto(5, 7)
print(f"El producto es: {resultado}")


